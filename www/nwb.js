// update cursor from crsrAttr values
function newCursor() {
    var
        o, z, ax1, ax2;

    if (crsr == null) {
        crsr = document.createElement('div');
        crsr.id = 'crsr';
        crsr.style.cssText = 'position:absolute;display:block;z-index:999';
        o = document.createElement('div');
        o.style.cssText = 'position:absolute;display:block;bottom:0px;left:0px;';
        crsr.appendChild(o);
        document.body.appendChild(crsr);
    } else 
        o = crsr.firstChild;

	z = getCrsrAttrOrientation(crsrAttr);
	ax1 = z ? 'height' : 'width';
	ax2 = z ? 'width' : 'height';
    o.style[ax2] = '100%';
    switch (getCrsrAttrSize(crsrAttr)) {
		case A_CRSR_NONE:   o.style[ax1] = '0%';   break;
        case A_CRSR_THIN:   o.style[ax1] = '10%';  break;
        case A_CRSR_THICK:  o.style[ax1] = '25%';  break;
        case A_CRSR_FULL:   o.style[ax1] = '100%'; break;
    }
    o.style['background-color'] = clut[getCrsrAttrColor(crsrAttr)];
}
