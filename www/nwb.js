// process keydowns (function, arrows, etc)
function keyDown(e) {
    var
        stateIdx,
        kc, ka;

    e = e || window.event;
    kc = e.keyCode || e.which;
    crsr.style.display = 'block';

    shiftState = e.shiftKey;
    ctrlState = e.ctrlKey;
    altState = e.altKey;
    setBulbs();

    stateIdx = (shiftState ? 1 : 0) + (ctrlState ? 2 : 0) + (altState ? 4 : 0);

    // translate for capslock
    if ((kc >= 65) && (kc <= 90) && (stateIdx < 2) && capState)
        stateIdx ^= 1;

    ka = keyvals[kc][stateIdx];
    if (ka == null) {
        // let browser handle it.
        return (e.returnValue = true);

    } else if (typeof ka == 'function') {
        ka();
    } else if (typeof ka == 'string') {
        // send string to console.
		if (ws.readyState == 1) ws.send(ka);
        conStrOut(ka);

        e.keyCode = 0;
        e.preventDefault();
        return (e.returnValue = true);

    } else if (typeof ka == 'number')
    {
        if (ka == 0) {
            // never do anything.
            e.preventDefault();
            return (e.returnValue = true);

        } else if (ka < 0) {
            // perform special action.
            switch (ka) {
                case DO_CAPLK:
                    capState = !capState;
                    setBulbs();
                    break;

                case DO_NUMLK:
                    numState = !numState;
                    setBulbs();
                    break;

                case DO_SCRLK:
                    scrState = !scrState;
                    setBulbs();
                    break;

                case DO_UP: // up
                    crsrUp();
                    break;

                case DO_DOWN: // down
                    crsrDown();
                    break;

                case DO_LEFT: // left
                    crsrLeft();
                    break;

                case DO_RIGHT: // right
                    crsrRight();
                    break;

                case DO_DELROW:
                    delRow(crsrRow);
                    crsrDraw();
                    if (popup != null) allToAnsi();
                    break;

                case DO_INSROW:
                    insRow(crsrRow);
                    crsrDraw();
                    if (popup != null) allToAnsi();
                    break;

                case DO_END:
                    crsrCol = conText[crsrRow].length;
                    crsrDraw();
                    break;

                case DO_HOME:
                    crsrCol = 0;
                    crsrDraw();
                    break;

                case DO_INSC:
                    insChar(crsrRow, crsrCol, 32);
                    break;

                case DO_DELC:
                    delChar(crsrRow, crsrCol);
                    break;

                case DO_POPUP:
                    if (popup == null) {
                        // create popup window
                        popup = window.open('edit.html', '', 'menubar=0,status=0,titlebar=0,toolbar=0');
                        popup.postMessage('ping','*');
                    } else {
                        popup.close();
                        popup = null;
                    }
                    break;

                case DO_TEST:
                    var
                        div = document.createElement('div'),
                        img = document.createElement('img');

                    div.className = 'sprite';
                    div.style['position'] = 'absolute';
                    div.style['left'] = crsr.style['left'];
                    div.style['top'] = crsr.style['top'];
                    div.style['width'] = (colSize * 12) + 'px';
                    div.style['height'] = (rowSize * 5) + 'px';
                    div.style['z-index'] = 999;
                    img.onload = fitSVGToDiv;
                    img.style['visibility'] = 'hidden';
                    img['src'] = svgFidoNet;
                    div.appendChild(img);
                    document.body.appendChild(div);
                    break;

                default:
                    // unknown action - pass to keyPress
                    return;
            }
            e.preventDefault();
            return (e.returnValue = false);

        } else if (ka > 0) {
            // send ascii
			if (ws.readyState == 1) ws.send(ka);
            conCharOut(ka);
			
            e.keyCode = 0;
            e.preventDefault();
            return (e.returnValue = false);
        }
    }
}

// process keypresses (alphas, numerics, etc)
function keyPress(e) {
    // normally, send to websocket server. only echo what is returned.
    var
        cc;

    e = e || window.event;
    cc = e.charCode;

    shiftState = e.shiftKey;
    ctrlState = e.ctrlKey;
    altState = e.altKey;

    capState = (((cc >= 65) && (cc <= 90) && !shiftState)
            || ((cc >= 97) && (cc <= 112) && shiftState));
    setBulbs();
}

// delete row from storage and element from html
function delRow(rownum) {
    var
        els, p;

    expandToRow(rownum);
    els = document.getElementsByClassName('vtx');
    p = els[rownum - 1].parentNode;
    p.removeChild(els[rownum]);
    conRowAttr.splice(rownum, 1);
    conText.splice(rownum, 1);
    conCellAttr.splice(rownum, 1);
}

// insert row into storage and element into html
function insRow(rownum) {
    var
        els, p;

    els = document.getElementsByClassName('vtx');
    p = els[rownum].parentNode;
    p.insertBefore(createNewRow(), els[rownum]);
    conRowAttr.splice(rownum, 0, defRowAttr);
    conText.splice(rownum, 0, '');
    conCellAttr.splice(rownum, 0, []);
}

// delete a character at position
function delChar(rownum, colnum) {
    expandToRow(rownum);
    expandToCol(rownum, colnum);
    conText[rownum] = conText[rownum].splice(colnum, 1);
    conCellAttr[rownum].splice(colnum, 1);
}

// insert a character at position. also sets attr to def
function insChar(rownum, colnum, chr) {
    expandToRow(rownum);
    expandToCol(rownum, colnum);
    conText[rownum] = conText[rownum].splice(colnum, 0, String.fromCharCode(chr));
    conCellAttr[rownum].splice(colnum, 0, defCellAttr);
    renderRow(rownum);
}

// create blank HTML row
function createNewRow() {
    var
        row = document.createElement('div');
    row.setAttribute('class', 'vtx');
    return row;
}

// poke a character
function conPutChar(rownum, colnum, chr, attr) {
    // expand if needed
    expandToRow(rownum);
    expandToCol(rownum, colnum);
    conText[rownum] = conText[rownum].splice(colnum, 1, String.fromCharCode(chr));
    conCellAttr[rownum][colnum] = attr;
}

// is this character a printable? (add )
function isprint(chr) {
    if (chr < 32) return false;                 // C0 controls
    if (chr > 126 && chr < 160) return false;   // C1 controls
    if (chr == 0xAD) return false;              // soft hyphen (173)
    return true;
}

// compute number of visible cells on this row.
function colsOnRow(rownum) {
    var
        cols = crtCols,
        size = getRowAttrSize(conRowAttr[rownum]) / 100;
        width = getRowAttrWidth(conRowAttr[rownum]) / 100;

    cols *= 1 / size;
    cols *= 1 / width;
    return cols;
}

// output character using current attribute at cursor position.
function conPrintChar(chr) {
    if (isprint(chr)) {
        // depends on this row width
        conPutChar(crsrRow, crsrCol, chr, currCellAttr);
        renderRow(crsrRow);
        crsrCol++;
        if (crsrCol == colsOnRow(crsrRow)) {
            crsrCol = 0;
            crsrRow++
        }
    }
}

function minMax(v, min, max, fallback) {
	if (fallback == null) {
		if (v < min) v = min;
		if (v > max) v = max;
	} else {
		if (v < min) v = fallback;
		if (v > max) v = fallback;
	}
	return v;
}

// flush out parameter array with defaults.				
// truncate to match defs length
function fixParams(parm, defs) {
	var
		i;
	for (i = parm.length; i < defs.length; i++)
		parm[i] = defs[i];
	return parm.slice(0, defs.length);
}

// the big function - ansi sequence state machine.
function conCharOut(chr) {
    var
        i, l,   		// generic idx, length
        r, c,   		// row, col idx
        render = false, // render row at end?
        doCSI = false,  // execute compiled CSI at end?
        doACP = false,  // execute compiled ACP at end?
        parm,
		div, img;		// for svg sprite creation

    // do all normal ctrls first
    switch (chr) {
        case 8:     // backspace
            if (crsrCol > 0) {
                expandToRow(crsrRow);
                expandToCol(crsrRow, crsrCol);
                crsrCol--;
                delChar(crsrRow, crsrCol);
                render = true;
            }
            break;

        case 9:     // horz tab
            crsrCol = ((crsrCol >> 3) + 1) << 3;
            if (crsrCol > colsOnRow(crsrRow))
                crsrCol = colsOnRow(crsrRow);
            break;

        case 10:    // linefeed
			if (!modeRealANSI)	// LF dont CR!  lol
				crsrCol = 0;	// for BBS/ANSI.SYS mode
            crsrRow++;
            break;

        case 13:    // carriage return
            crsrCol = 0;
            break;

        case 127:   // delete
            expandToRow(crsrRow);
            expandToCol(crsrRow, crsrCol);
            delChar(crsrRow, crsrCol);
            render = true;
            break;

        default:
            switch (ansiState) {
                case 0:
                    // not in an sequence.
                    if (chr == 27)
                        ansiState = 1
                    else
                        conPrintChar(chr);
                    break;

                case 1:
                    // start of ansi sequence
                    if (chr == 0x5B) {
                        // ESC [ - CSI
                        parms = '';
                        interm = '';
                        ansiState = 2
                    }
                    else if (chr == 0x23)
                        // ESC # - row attr
                        ansiState = 3
                    else if (chr == 0x5F)
                        // ESC _ - sprite def
                        ansiState = 4
                    else
                        // unrecognized - abort sequence
                        ansiState = 0;
                    break;

                case 2:
                    // start of CSI (ESC [)
                    // collect parameters until either intermediate or final
                    if ((chr >= 0x30) && (chr <= 0x3F))
                        parms += String.fromCharCode(chr)
                    else if ((chr >= 0x20) && (chr <= 0x2F)) {
                        // intermediate byte
                        interm = String.fromCharCode(chr);
                        ansiState = 5;
                    } else if ((chr >= 0x40) && (chr <= 0x7E)) {
                        // final byte
                        ansiState = 0;
                        doCSI = true;
                    } else
                        // unrecognized - abort sequence
                        ansiState = 0;
                    break

                case 3:
                    // start of row attr (ESC #)
                    // get single byte (0,1,9)
                    if (chr == 0x30 || chr == 0x31) {
                        // marquee off/on
                        conRowAttr[crsrRow] = setRowAttrMarquee(conRowAttr[crsrRow], (chr - 0x30));
                        render = true;
                    } else if (chr == 0x39) {
                        // reset row.
                        conRowAttr[crsrRow] = defRowAttr;
                        render = true;
                    } // else unrecognized
                    ansiState = 0;
                    break;

                case 4:
                    // start of sprite def APC (ESC _)
                    // read until ST (ESC \)
                    // '0' [ ; n [ ; base64 ]] ST
                    if ((chr >= 0x20) && (chr <= 0x7E))
                        acpstr += String.fromCharCode(chr)
                    else if (chr == 0x1B)
                        // advance to finish reading string terminator (ST)
                        ansiState = 6
                    else
                        // unrecognized - abort sequence
                        ansiState = 0;
                    break;

                case 5:
                    // collecting intermediate bytes
                    if ((chr >= 0x20) && (chr <= 0x2F))
                        interm += String.fromCharCode(chr)
                    else if ((chr >= 0x40) && (chr <= 0x7E)) {
                        // command?
                        ansiState = 0;
                        doCSI = true;
                    } else
                        // unrecognized - abort sequence
                        ansiState = 0;
                    break;

                case 6:
                    // confirm ST on APC sprite def
                    if (chr == 0x5C) {
                        // valid ST - process
                        doAPC = true;
                    }
                    ansiState = 0;
                    break;
            }
            break;
    }

    if (doCSI) {
        // chr = command / final byte
        // params = optional parameters
        // interm = optional intermediate (not using any for this term emulation - ignore)

        parm = parms.split(';');
        if (parm[0] == '') parm = [];

        // for our purposes, all parameters are integers. if not, leave as string.
        l = parm.length;
        for (i = 0; i < l; i++){
            v = parseInt(parm[i]);
            if (!isNaN(v))
                parm[i] = v;
        }

        switch (chr) {
            case 0x48:  // H - Cursor Position
            case 0x66:  // f - Cursor Position
                // set missing to defaults of 1
				parm = fixParams(parm, [ 1, 1 ]);
				parm[0] = minMax(parm[0], 1, 999);
				parm[1] = minMax(parm[1], 1, 999);
                while (l < 2)
                    parm[l++] = 1;
                crsrRow = parm[0] - 1;
                crsrCol = parm[1] - 1;
                break;

            case 0x68:  // h - set mode
            case 0x6C:  // l - reset mode
                parm[0] = parm[0].toString();
                switch (parm[0]) {
                    case '?50':
                        modeRealANSI = (chr == 0x68);
                        break;
                }
                break;

            case 0x41:  // A - Cursor Up
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, 999);
                crsrRow -= parm[0];
                if (crsrRow < 0)
                    crsrRow = 0;
                break;

            case 0x42:  // B - Cursor Down
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, 999);
                crsrRow += parm[0];
                break;

            case 0x43:  // C - Cursor Forward
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, 999);
                crsrCol += parm[0];
                if (crsrCol >= colsOnRow(crsrRow) - 1)
                    crsrCol = colsOnRow(crsrRow) - 1;
                break;

            case 0x44:  // D - Cursor Backward
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, 999);
                crsrCol -= parm[0];
                if (crsrCol < 0)
                    crsrCol = 0;
                break;

            case 0x73:  // s - Save Position
                crsrSaveRow = crsrRow;
                crsrSaveCol = crsrCol;
                break;

            case 0x75:  // u - Restore Position
                crsrRow = crsrSaveRow;
                crsrCol = crsrSaveCol;
                break;

            case 0x45:  // E - Next Line
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, 999);
                crsrCol = 0;
                crsrRow += parm[0];
                break;

            case 0x46:  // F - Previous Line
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, 999);
                crsrCol = 0;
                crsrRow -= parm[0];
                if (crsrRow < 0)
                    crsrRow = 0;
                break;

            case 0x47:  // G - To Column
				parm = fixParams(parm, [1]);
				parm[0] = minMax(parm[0], 1, getMaxCol(crsrRow), 1);
                crsrCol = parm[0] - 1;
                break;

            case 0x4A:  // J - Erase in Screen (0=EOS,1=SOS,2=ALL)
				parm = fixParams(parm, [0]);
				parm[0] = minMax(parm[0], 0, 2, 0);
                expandToRow(crsrRow);
                expandToCol(crsrRow, crsrCol);
                switch (parm[0]) {
                    case 0:
                        // clear EOL first
                        conCellAttr[crsrRow].length = crsrCol;
                        conText[crsrRow] = conText[crsrRow].substring(0, crsrCol);
                        renderRow(crsrRow);
                        // clear EOS
                        for (r = getMaxRow(); r > crsrRow; r--) {
                            row = getRowElement(r);
                            row.parentNode.removeChild(row);
                            conRowAttr.length = crsrRow + 1;
                            conCellAttr.length = crsrRow + 1;
                            conText.length = crsrRow + 1;
                        }
                        break;

                    case 1:
                        // clear SOL first
                        for (c = 0; c <= crsrCol; c++)
                            conPutChar(crsrRow, c, 32, defCellAttr);
                        renderRow(crsrRow);
                        // clear SOS
                        for (r = 0; r < crsrRow; r++) {
                            conRowAttr[r] = defRowAttr;
                            conCellAttr[r] = [];
                            conText[r] = '';
                            renderRow(r);
                        }
                        break;

                    case 2:
                        // clear entire screen.
                        // remove html rows
                        els = document.getElementsByClassName('vtx');
                        for (l = els.length, r = l - 1; r >= 0; r--)
                            els[r].parentNode.removeChild(els[r]);
                        // remove sprites
                        els = document.getElementsByClassName('sprite');
                        for (l = els.length, r = l - 1; r >= 0; r--)
                            els[r].parentNode.removeChild(els[r]);
                        // reset console
                        conRowAttr = [];
                        conCellAttr = [];
                        conText = [];
                        if (!modeRealANSI)
                            crsrRow = crsrCol = 0   // BBS / ANSI.SYS
                        else {
                            expandToRow(crsrRow);   // ECMA-048 complient
                            expandToCol(crsrRow, crsrCol);
                        }
                        currCellAttr = defCellAttr;
                        renderRow(-1);
                        break;
                }
                break;

            case 0x4B:  // K - Erase in Line
				parm = fixParams(parm, [0]);
				parm[0] = minMax(parm[0], 0, 2, 0);
                expandToRow(crsrRow);
                expandToCol(crsrRow, crsrCol);
                switch (parm[0]) {
                    case 0:
                        // clear EOL first
                        conCellAttr[crsrRow].length = crsrCol;
                        conText[crsrRow] = conText[crsrRow].substring(0, crsrCol);
                        break;

                    case 1:
                        // clear SOL first
                        for (c = 0; c <= crsrCol; c++)
                            conPutChar(crsrRow, c, 32, defCellAttr);
                        break;

                    case 2:
                        // clear row.
                        conText[crsrRow] = '';
                        conCellAttr[crsrRow] = [];
                        break;
                }
                render = true;
                break;

            case 0x5F:  // _ - Display/Hide Sprite | CSI s ; n ; w ; h ; z _
				parm = fixParams(parm, [ 1, 1, 1, 1, 0 ]);
				parm[0] = minMax(parm[0], 1, 64, 1);	// sprint #
				parm[1] = minMax(parm[1], 1, 64, 1);	// def #
				parm[2] = minMax(parm[2], 1, 999, 1);	// w
				parm[3] = minMax(parm[3], 1, 999, 1);	// h
				parm[4] = minMax(parm[4], 0, 1, 0);		// z
				if (l == 0) {
					// remove all sprites
					els = document.getElementsByClassName('sprite');
					for (i = els.length - 1; i >= 0; i--)
						els[i].parentNode.remove(els[i]);
				} else if (l == 1) {
					// remove one sprite
					div = document.getElementById('sprite' + parm[0]);
					if (div != null) 
						div.parentNode.remove(div);
				} else {
					// display a new sprite
					// remove old one if it exists first
					div = document.getElementById('sprite' + parm[0]);
					if (div == null) {
						// reuse the old one.
						img = div.firstChild;
					} else {
						// make a new one.
						div = document.createElement('div');
						img = document.createElement('img');
						div.className = 'sprite';
						div.id = 'sprite' + parm[0];	// sprite number
					}
					div.style.cssText = 'position:absolute;'
						+ 'left:' + crsr.style['left'] + ';'
						+ 'top:' + crsr.style['top'] + ';' 
						+ 'width:' + (colSize * parm[2]) + 'px;' 
						+ 'height' + (rowSize * parm[3]) + 'px;'
						+ 'z-index:' + (parm[4] ? '-999':'999');
					img.onload = fitSVGToDiv;
					img.style.cssText = 'visibility:hidden;';
					img['src'] = svgData;
					div.appendChild(img);
					document.body.appendChild(div);
				}
				break;
				
            case 0x5E:  // ^ - Cursor Modes
				parm = fixParams(parm, [7, 2, 0]);
				parm[0] = minMax(parm[0], 0, 255, 7);
				parm[1] = minMax(parm[1], 0, 3, 2);
				parm[2] = minMax(parm[2], 0, 1, 0);
				crsrAttr = makeCrsrAttr(parm[0], parm[1] )
                break;

            case 0x5B:  // [ - Row Size
				parm = fixParams(parm, [3,1]);
				parm[0] = minMax(parm[0], 0, 7, 3);
				parm[1] = minMax(parm[1], 0, 3, 1);
                conRowAttr[crsrRow] = setRowAttrSize(conRowAttr[crsrRow],
                    (parm[0] + 1) * 25);
                conRowAttr[crsrRow] = setRowAttrWidth(conRowAttr[crsrRow],
                    (parm[1] + 1) * 50);
                render = true;
                break;

            case 0x5D:  // ] - Row Modes / background
				parm = fixParams(parm, [0,0,0]);
				parm[0] = minMax(parm[0], 0, 255, 0);
				parm[1] = minMax(parm[1], 0, 255, 0);
				parm[2] = minMax(parm[2], 0, 3, 0);
                conRowAttr[crsrRow] = setRowAttrColor1(conRowAttr[crsrRow], parm[0]);
                conRowAttr[crsrRow] = setRowAttrColor2(conRowAttr[crsrRow], parm[1]);
                conRowAttr[crsrRow] = setRowAttrPattern(conRowAttr[crsrRow], parm[2] << 16);
                render = true;
                break;

            case 0x6D:  // m - Character Attr
				if (l < 1) parm[0] = 0;	// don't use fixparms. variable parameters.
				parm[0] = minMax(parm[0], 0, 255);
                for (i = 0; i < l; i++) {
                    switch (parm[i]) {
                        case 0:     // reset
                            currCellAttr = defCellAttr;
                            break;

                        case 1:     // bold on / off
                        case 21:
                            currCellAttr =
                                setCellAttrBold(currCellAttr, (parm[i] == 1));
                            break;

                        case 3:     // italics on/off
                        case 23:
                            currCellAttr =
                                setCellAttrItalics(currCellAttr, (parm[i] == 3));
                            break;

                        case 4:     // underline
                        case 24:
                            currCellAttr =
                                setCellAttrUnderline(currCellAttr, (parm[i] == 4));
                            break;

                        case 5:     // blink
                        case 25:
                            currCellAttr =
                                setCellAttrBlink(currCellAttr, (parm[i] == 5));
                            break;

                        case 7:     // revers video
                        case 27:
                            currCellAttr =
                                setCellAttrReverse(currCellAttr, (parm[i] == 7));
                            break;

                        case 8:     // conceal
                        case 28:
                            currCellAttr =
                                setCellAttrConceal(currCellAttr, (parm[i] == 8));
                            break;

                        case 9:     // strikethrough
                        case 29:
                            currCellAttr =
                                setCellAttrStrikethrough(currCellAttr, (parm[i] == 9));
                            break;

                        case 50:    // glow
                        case 70:
                            currCellAttr =
                                setCellAttrGlow(currCellAttr, (parm[i] == 50));
                            break;

                        case 56:    // outline
                        case 76:
                            currCellAttr =
                                setCellAttrOutline(currCellAttr, (parm[i] == 56));
                            break;

                        case 57:    // shadow
                        case 77:
                            currCellAttr =
                                setCellAttrShadow(currCellAttr, (parm[i] == 57));
                            break;

                        // text foreground colors
                        case 30: case 31: case 32: case 33:
                        case 34: case 35: case 36: case 37:
                            // foreground color (0-7)
                            currCellAttr = setCellAttrFG(currCellAttr, parm[i] - 30)
                            break;

                        case 38:
                            // check for 5 ; color
                            if (++i < l)
                                if (parm[i] == 5)
                                    if (++i < l) {
										parm[i] = minMax(parm[1], 0, 255, 7);
                                        currCellAttr = setCellAttrFG(currCellAttr, parm[i]);
									}
                            break;

                        case 39:
                            // default
                            currCellAttr =
                                setCellAttrFG(currCellAttr, getCellAttrFG(defCellAttr));
                            break;

                        case 90: case 91: case 92: case 93:
                        case 94: case 95: case 96: case 97:
                            // foreground color (8-15)
                            currCellAttr = setCellAttrFG(currCellAttr, parm[i] - 90 + 8);
                            break;

                        // text background colors
                        case 40: case 41: case 42: case 43:
                        case 44: case 45: case 46: case 47:
                            // background color (0-7)
                            currCellAttr = setCellAttrBG(currCellAttr, parm[i] - 40)
                            break;

                        case 48:
                            // check for 5 ; color
                            if (++i < l)
                                if (parm[i] == 5)
                                    if (++i < l) {
										parm[i] = minMax(parm[1], 0, 255, 7);
                                        currCellAttr = setCellAttrBG(currCellAttr, parm[i]);
									}
                            break;

                        case 49:
                            // default
                            currCellAttr =
                                setCellAttrBG(currCellAttr, getCellAttrBG(defCellAttr));
                            break;

                        case 100: case 101: case 102: case 103:
                        case 104: case 105: case 106: case 107:
                            // background color (8-15)
                            currCellAttr = setCellAttrBG(currCellAttr, parm[i] - 100 + 8);
                            break;
                    }
                }
                break;

            default:
                // unsupported - ignore
                break;
        }
    } else if (doACP) {
        // acpstr = string command - define sprites
		// string = 0;n;base64...
		parm = acpstr.split(';');
		switch (parm.length) {
			case 1:
				// clear all definitions
				spriteDefs = [];
				break;
				
			case 2:
				// clear a single sprite
				spriteDefs[parm[1]] = null;
				break;
				
			case 3:
				// define a sprite
				spriteDefs[parm[1]] = parm[2];
				break;
		} // else ignore
    }

    if (render)
        renderRow(crsrRow);
    crsrDraw();
}

