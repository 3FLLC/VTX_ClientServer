// for communicating with popup editor window.
addEventListener('message', function (e) {
    var str = e.data;
    var cmd = str.substring(0,3);
    var val = str.substring(4);
    var v, i;

    e = e || window.event;
    switch (cmd) {
        case 'STR':
            conStrOut(val);
            break;

        case 'CFG':
            currCellAttr = setCellAttrFG(currCellAttr, clut.indexOf(val));
            break;

        case 'CBG':
            currCellAttr = setCellAttrBG(currCellAttr, clut.indexOf(val));
            break;

        case 'RC1':
            conRowAttr[crsrRow] = setRowAttrColor1(conRowAttr[crsrRow], clut.indexOf(val));
            renderRow(crsrRow);
            crsrDraw();
            break;

        case 'RC2':
            conRowAttr[crsrRow] = setRowAttrColor2(conRowAttr[crsrRow], clut.indexOf(val));
            renderRow(crsrRow);
            crsrDraw();
            break;

        case 'ATR':
            switch (val) {
                case 'Bold':            currCellAttr ^= A_CELL_BOLD; break;
                case 'Italics':         currCellAttr ^= A_CELL_ITALICS; break;
                case 'Underline':       currCellAttr ^= A_CELL_UNDERLINE; break;
                case 'Blink':           currCellAttr ^= A_CELL_BLINK; break;
                case 'Shadow':          currCellAttr ^= A_CELL_SHADOW; break;
                case 'Strikethrough':   currCellAttr ^= A_CELL_STRIKETHROUGH; break;
                case 'Outline':         currCellAttr ^= A_CELL_OUTLINE; break;
                case 'Glow':            currCellAttr ^= A_CELL_GLOW; break;
                case 'Reverse':         currCellAttr ^= A_CELL_REVERSE; break;
                case 'Conceal':         currCellAttr ^= A_CELL_CONCEAL; break;
                case 'Marquee':
                    conRowAttr[crsrRow] ^= A_ROW_MARQUEE;
                    renderRow(crsrRow);
                    crsrDraw();
                    break;
            }
            break;

        case 'SIZ':
            conRowAttr[crsrRow] = setRowAttrSize(conRowAttr[crsrRow], parseInt(val));
            renderRow(crsrRow);
            crsrDraw();
            break;

        case 'WID':
            conRowAttr[crsrRow] = setRowAttrWidth(conRowAttr[crsrRow], parseInt(val));
            renderRow(crsrRow);
            crsrDraw();
            break;

        case 'ROW':
            conRowAttr[crsrRow] = setRowAttrPattern(conRowAttr[crsrRow], parseInt(val)<<16);
            renderRow(crsrRow);
            crsrDraw();
            break;

        case 'CRS':
            crsrAttr = parseInt(val);
            newCursor();
            break;
    }
});

// convert screen to ansi
function allToAnsi() {
    var
        r, c, l,
        p1, p2, p3,
        txt, attr, lastattr,
        ansi = '',

    lastattr = defCellAttr;
    for (r = 0, l = getMaxRow(); r <= l; r++) {

        // BUILD ROW ATTRIBUTES IF NOT DEFAULT
        if (conText[r].length > 0) {
            attr = conRowAttr[r];
            if (getRowAttrMarquee(attr))
                ansi += ESC + '#1';

            p1 = (getRowAttrSize(attr) / 25) - 1;
            p2 = (getRowAttrWidth(attr) / 50) - 1;
            if (p1 != 3 || p2 != 1) {
                if (p2 == 1)
                    ansi += CSI + p1 + '[';
                else
                    ansi += CSI + p1 + ';' + p2 + '[';
            }
            p1 = getRowAttrColor1(attr);
            p2 = getRowAttrColor2(attr);
            p3 = getRowAttrPattern(attr) >> 16;
            if (p1 != 7 || p2 != 0 || p3 != 0) {
                if (p3 == 0) {
                    if (p2 == 0)
                        ansi += CSI + p1 + ']';
                    else
                        ansi += CSI + p1 + ';' + p2 + ']';
                } else {
                    ansi += CSI + p1 + ';' + p2 + ';' + p3 + ']';
                }
            }

            for (c = 0; c < conText[r].length; c++) {
                //buid character attribute codes
                attr = conCellAttr[r][c];
                if (lastattr != attr) {
                    // code needed - determine difference
                    ansi += CSI;
                    p1 = getCellAttrFG(attr);
                    if (p1 != getCellAttrFG(lastattr))
                        if (p1 < 8)
                            ansi += (30 + p1) + ';'
                        else if (p1 < 16)
                            ansi += (90 + p1 - 8) + ';'
                        else
                            ansi += '38;5;' + p1 + ';';
                    p1 = getCellAttrBG(attr);
                    if (p1 != getCellAttrBG(lastattr))
                        if (p1 < 8)
                            ansi += (40 + p1) + ';'
                        else if (p1 < 16)
                            ansi += (100 + p1 - 8) + ';'
                        else
                            ansi += '48;5;' + p1 + ';';
                    p1 = getCellAttrBold(attr);
                    if (p1 != getCellAttrBold(lastattr))
                        ansi += p1 ? '1;' : '21;';
                    p1 = getCellAttrItalics(attr);
                    if (p1 != getCellAttrItalics(lastattr))
                        ansi += p1 ? '3;' : '23;';
                    p1 = getCellAttrUnderline(attr);
                    if (p1 != getCellAttrUnderline(lastattr))
                        ansi += p1 ? '4;' : '24;';
                    p1 = getCellAttrBlink(attr);
                    if (p1 != getCellAttrBlink(lastattr))
                        ansi += p1 ? '5;' : '25;';
                    p1 = getCellAttrStrikethrough(attr);
                    if (p1 != getCellAttrStrikethrough(lastattr))
                        ansi += p1 ? '9;' : '29;';
                    p1 = getCellAttrGlow(attr);
                    if (p1 != getCellAttrGlow(lastattr))
                        ansi += p1 ? '50;' : '70;'; //*
                    p1 = getCellAttrOutline(attr);
                    if (p1 != getCellAttrOutline(lastattr))
                        ansi += p1 ? '56;' : '76;'; //*
                    p1 = getCellAttrShadow(attr);
                    if (p1 != getCellAttrShadow(lastattr))
                        ansi += p1 ? '57;' : '77;'; //*
                    ansi += 'm';
                    lastattr = attr;
                }
                ansi += conText[r].charAt(c);
            }
        }
        if (r < l)
            ansi += CRLF;
    }
    popup.postMessage(ansi, '*');
}

