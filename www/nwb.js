// brighten / darken a color. color is a 24bit value (0xRRGGBB)
function brightenRGB(colorstr, factor) {
    var
        r, g, b,
        rgb = htoi(colorstr);

    r = (rgb >> 16) & 0xFF;
    g = (rgb >>  8) & 0xFF;
    b =  rgb        & 0xFF;
    if (factor < 0) {
        factor += 1.0;
        r *= factor;
        g *= factor;
        b *= factor;
    } else {
        r = (255 - r) * factor + r;
        g = (255 - g) * factor + g;
        b = (255 - b) * factor + b;
    }
    r = Math.floor(r) & 0xFF;
    g = Math.floor(g) & 0xFF;
    b = Math.floor(b) & 0xFF;
    return  itoh((r << 16) + (g << 8) + b, 6);
}

